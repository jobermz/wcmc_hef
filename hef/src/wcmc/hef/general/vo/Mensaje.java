package wcmc.hef.general.vo;

public class Mensaje {
	private String strPara;
	private String strDe;
	private String strAsunto;
	private String strCuerpo;
	
	public String getStrPara() {
		return strPara;
	}
	public void setStrPara(String strPara) {
		this.strPara = strPara;
	}
	public String getStrDe() {
		return strDe;
	}
	public void setStrDe(String strDe) {
		this.strDe = strDe;
	}
	public String getStrAsunto() {
		return strAsunto;
	}
	public void setStrAsunto(String strAsunto) {
		this.strAsunto = strAsunto;
	}
	public String getStrCuerpo() {
		return strCuerpo;
	}
	public void setStrCuerpo(String strCuerpo) {
		this.strCuerpo = strCuerpo;
	}
	
}
